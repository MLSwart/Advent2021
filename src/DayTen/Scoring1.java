    public int stackPush(String[] input) {

        int score = 0;
        for (int j = 0; j < input.length; j++) {
            Stack<Character> characterStack = new Stack<>();
            char[] charArray = input[j].toCharArray();
            for (int i = 0; i < charArray.length; i++) {
                if (charArray[i] == '(' || charArray[i] == '<' || charArray[i] == '{' || charArray[i] == '[') {
                    characterStack.push(charArray[i]);
                }
                if (charArray[i] == ')' || charArray[i] == '>' || charArray[i] == '}' || charArray[i] == ']') {
                    char Top = characterStack.pop(); //Top is top vd stack, charArray[i] is de nieuwe waarde die we controleren tegen de stack
                    if (Top == '(' && charArray[i] == ')') {
                        continue;
                    }
                    if (Top == '[' && charArray[i] == ']') {
                        continue;
                    }
                    if (Top == '<' && charArray[i] == '>') {
                        continue;
                    }
                    if (Top == '{' && charArray[i] == '}') {
                        continue;
                    }
                    System.out.println("Error. Unclosed: " + Top + " , found " + charArray[i]);
                    if (charArray[i] == ')') {
                        score = score + 3;
                        break;
                    }
                    if (charArray[i] == ']') {
                        score = score + 57;
                        break;
                    }
                    if (charArray[i] == '}') {
                        score = score + 1197;
                        break;
                    }
                    if (charArray[i] == '>') {
                        score = score + 25137;
                        break;
                    }


                }
            }


        }
        return score;
    }
